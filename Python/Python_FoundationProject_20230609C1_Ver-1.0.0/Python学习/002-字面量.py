# 整数字面量
x = 42
print(x)

# 浮点数字面量
y = 3.14
print(y)

# 字符串字面量
name = 'John'
print(name)

# 布尔值字面量
is_true = True
print(is_true)

# 空值字面量
empty_value = None
print(empty_value)

# 列表字面量
numbers = [1, 2, 3, 4, 5]
print(numbers)

# 元组字面量
point = (10, 20)
print(point)

# 字典字面量
person = {'name': 'John', 'age': 30}
print(person)

